    @staticmethod
    def _collect_approval_reasons(
        *,
        safety_checks: list[SafetyCheck],
        status: StrategyStatus,
        policy_validation: PolicyValidation,
    ) -> list[str]:
        """Build the human-readable list shown on the approval card.

        Order: each failed safety check's ``label: detail`` first, followed
        by status / policy level reasons that don't map to a single check.
        Always returns ``[]`` when nothing forces human approval — keeps the
        UI minimal on the happy path.
        """
        reasons: list[str] = [
            f"{c.label}: {c.detail}" for c in safety_checks if not c.passed
        ]
        if status == StrategyStatus.NEEDS_REVISION:
            reasons.append(
                "Policy validation failed; strategy needs revision before any execution"
            )
        elif status == StrategyStatus.REJECTED:
            reasons.append("Strategy was rejected by Coordinator")
        if (
            policy_validation.mcp_called
            and policy_validation.mcp_error
            and not any("policy" in r.lower() for r in reasons)
        ):
            reasons.append(f"policy MCP error: {policy_validation.mcp_error}")
        return reasons

    @staticmethod
    def _append_human_approval_constraints(
        constraints: list[str],
        *,
        human_approval_required: bool,
        auto_execution_allowed: bool,
        approval_reasons: list[str],
    ) -> None:
        """Push the strongest "do not auto-execute" line when applicable.

        Avoids duplicating an equivalent line that ``_append_policy_execution_constraints``
        may have already pushed (which only fires for status-driven blocking).
        """
        if auto_execution_allowed:
            return
        if not human_approval_required:
            return
        already_blocked = any(
            "auto-execution" in c.lower() and ("blocked" in c.lower() or "disabled" in c.lower())
            for c in constraints
        )
        if not already_blocked:
            top_reason = approval_reasons[0] if approval_reasons else "policy / safety check failed"
            constraints.append(
                f"Auto-execution disabled: human approval required ({top_reason})"
            )

