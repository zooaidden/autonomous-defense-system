    @staticmethod
    def _aggregate_mcp_trace(state: DebateState) -> list[MCPToolCall]:
        """按发生顺序聚合 Planner / Red-Team 两侧的 MCP 调用。

        - 没有任何 MCP 调用时返回空数组（满足 spec：mcp_trace: []）
        - 不去重：保留每次调用的真实顺序，利于审计与前端时间线展示
        """
        trace: list[MCPToolCall] = []
        if state.plannerMetadata and state.plannerMetadata.mcp_tool_calls:
            trace.extend(state.plannerMetadata.mcp_tool_calls)
        if state.redTeamFindings and state.redTeamFindings.mcp_tool_calls:
            trace.extend(state.redTeamFindings.mcp_tool_calls)
        return trace

    @staticmethod
    def _build_topology_context_summary(state: DebateState) -> dict[str, Any]:
        """聚合 Planner + Red-Team 拓扑视角，输出供前端 / 下游消费的紧凑摘要。"""
        md: PlannerTopologyMetadata | None = state.plannerMetadata
        ft: RedTeamFindings | None = state.redTeamFindings

        used = bool(md and md.topology_context_used)
        summary: dict[str, Any] = {
            "topology_context_used": used,
            "expected_blast_radius": (md.expected_blast_radius.value if md else BlastRadius.LOW.value),
            "affected_assets": list(md.affected_assets) if md else [],
            "topology_evidence": list(md.topology_evidence) if md else [],
            "red_team": {
                "topology_findings": list(ft.topology_based_findings) if ft else [],
                "residual_attack_paths": [
                    {
                        "source": p.source,
                        "target": p.target,
                        "nodes": list(p.nodes),
                        "summary": p.summary,
                    }
                    for p in (ft.residual_attack_paths if ft else [])
                ],
                "business_impact_risks": list(ft.business_impact_risks) if ft else [],
                "recommended_constraints": list(ft.recommended_constraints) if ft else [],
            },
            "mcp_errors": [
                err for err in (
                    (md.mcp_error if md else None),
                    (ft.mcp_error if ft else None),
                )
                if err
            ],
        }
        return summary

    @staticmethod
    def _build_execution_constraints(state: DebateState) -> list[str]:
        """聚合给执行层的硬约束清单。

        来源：
          1) Red-Team recommended_constraints（已经是 actionable 的字符串）
          2) Planner expected_blast_radius=HIGH 的额外提示（要求灰度发布）
          3) 未解决挑战 -> 执行需观察 N 分钟后再扩量
        """
        constraints: list[str] = []
        ft = state.redTeamFindings
        if ft and ft.recommended_constraints:
            constraints.extend(ft.recommended_constraints)

        md = state.plannerMetadata
        if md and md.expected_blast_radius == BlastRadius.HIGH:
            constraints.append(
                "Apply with canary rollout: enable on 10% traffic for 5 minutes "
                "before full deployment due to HIGH expected blast radius"
            )

        unresolved_count = len(state.unresolvedChallenges)
        if unresolved_count > 0:
            constraints.append(
                f"Monitor {unresolved_count} unresolved challenge(s) for 10 minutes "
                f"post-deployment; auto-rollback if anomaly detected"
            )
        return constraints

    @staticmethod
    def _compute_risk_level(state: DebateState, strategy: Any) -> RiskLevel:
        """综合多维信号计算最终风险等级。

        信号权重（取最大值）：
          - SecurityEvent.severity (CRITICAL -> CRITICAL, HIGH -> HIGH, MEDIUM -> MEDIUM, LOW -> LOW)
          - PlannerTopologyMetadata.expected_blast_radius
          - RedTeamFindings: residual_attack_paths>0 -> HIGH;
                              business_impact_risks>=2 -> HIGH;
                              business_impact_risks=1 -> MEDIUM
          - SecurityEvent.riskScore >= 0.9 -> 至少 HIGH
          - 未解决挑战数 >= 3 -> 至少 MEDIUM
        """
        level = RiskLevel.LOW

        # 1) 事件 severity 直接映射
        sev_map = {
            Severity.LOW: RiskLevel.LOW,
            Severity.MEDIUM: RiskLevel.MEDIUM,
            Severity.HIGH: RiskLevel.HIGH,
            Severity.CRITICAL: RiskLevel.CRITICAL,
        }
        level = CoordinatorAgent._max_risk(level, sev_map.get(state.securityEvent.severity, RiskLevel.LOW))

        # 2) Planner blast radius
        if state.plannerMetadata:
            br = state.plannerMetadata.expected_blast_radius
            if br == BlastRadius.HIGH:
                level = CoordinatorAgent._max_risk(level, RiskLevel.HIGH)
            elif br == BlastRadius.MEDIUM:
                level = CoordinatorAgent._max_risk(level, RiskLevel.MEDIUM)

        # 3) Red-team topology signals (residual paths may stay <= MEDIUM for sandbox lows).
        ft = state.redTeamFindings
        if ft:
            if ft.residual_attack_paths:
                if CoordinatorAgent._sandbox_residual_paths_should_not_escalate_to_high(state):
                    level = CoordinatorAgent._max_risk(level, RiskLevel.MEDIUM)
                else:
                    level = CoordinatorAgent._max_risk(level, RiskLevel.HIGH)
            biz_count = len(ft.business_impact_risks)
            if biz_count >= 2:
                level = CoordinatorAgent._max_risk(level, RiskLevel.HIGH)
            elif biz_count == 1:
                level = CoordinatorAgent._max_risk(level, RiskLevel.MEDIUM)

        # 4) riskScore 高位
        if state.securityEvent.riskScore >= 0.9:
            level = CoordinatorAgent._max_risk(level, RiskLevel.HIGH)

        # 5) 未解决挑战堆积
        if len(state.unresolvedChallenges) >= 3:
            level = CoordinatorAgent._max_risk(level, RiskLevel.MEDIUM)

        return level

    @staticmethod
    def _sandbox_residual_paths_should_not_escalate_to_high(state: DebateState) -> bool:
        """Keep residual_attack_paths from forcing HIGH when blast/event tier are sandbox-low."""

        md = state.plannerMetadata
        if not md or md.expected_blast_radius != BlastRadius.LOW:
            return False
        if state.securityEvent.severity not in (Severity.LOW, Severity.MEDIUM):
            return False
        if state.securityEvent.riskScore > 0.3:
            return False
        return True

    @staticmethod
    def _has_high_tier_asset_impact_signal(state: DebateState) -> bool:
        """True when CRITICAL/HIGH-tier asset impact is indicated (blocks low-risk auto)."""

        ft = state.redTeamFindings
        if ft:
            for line in ft.topology_based_findings:
                u = line.upper()
                if "CRITICAL" in u and ("ASSET" in u or "CRITICALITY" in u or "IMPACT" in u):
                    return True
                if "HIGH" in u and "CRITICALITY" in u:
                    return True
            for line in ft.business_impact_risks:
                u = line.upper()
                if "CRITICAL" in u:
                    return True
                if "HIGH" in u and "CRITICALITY" in u:
                    return True
        if state.securityEvent.severity in (Severity.HIGH, Severity.CRITICAL):
            return True
        md = state.plannerMetadata
        return bool(md and md.expected_blast_radius == BlastRadius.HIGH)

    @staticmethod
    def _is_delete_resource_action(action: Any) -> bool:
        params = dict(action.parameters or {})
        op = str(params.get("operation") or params.get("mode") or "").lower()
        return op in {"delete", "delete_resource", "remove", "destroy"}

    @staticmethod
    def _strategy_has_blocked_destructive_actions(strategy: DefenseStrategy) -> bool:
        """Blocks auto-exec when destructive verbs appear on high-impact actions."""

        for action in strategy.actions:
            if CoordinatorAgent._is_firewall_deny_all(action):
                return True
            if CoordinatorAgent._is_scale_down_critical(action):
                return True
            if CoordinatorAgent._is_delete_resource_action(action):
                return True
        return False

    @staticmethod
    def _rollback_ttl_eligible(strategy: DefenseStrategy) -> bool:
        plan = strategy.rollbackPlan
        steps_ok = bool(plan and plan.steps)
        trigger_ok = bool(plan and (plan.triggerCondition or "").strip())
        ttl_ok = int(strategy.ttl or 0) >= 60
        return steps_ok and trigger_ok and ttl_ok

    @staticmethod
    def _is_low_risk_auto_executable(
        *,
        state: DebateState,
        strategy: DefenseStrategy,
        policy_validation: PolicyValidation,
        risk_level: RiskLevel,
    ) -> bool:
        """Predicate for sandbox/demo flows that may bypass manual approval gates."""

        if not policy_validation.mcp_called or not policy_validation.valid:
            return False
        if policy_validation.requires_human_approval:
            return False
        if state.securityEvent.severity not in (Severity.LOW, Severity.MEDIUM):
            return False
        if state.securityEvent.riskScore > 0.3:
            return False
        md = state.plannerMetadata
        if not md or md.expected_blast_radius != BlastRadius.LOW:
            return False
        if CoordinatorAgent._has_high_tier_asset_impact_signal(state):
            return False
        if risk_level not in (RiskLevel.LOW, RiskLevel.MEDIUM):
            return False
        if CoordinatorAgent._strategy_has_blocked_destructive_actions(strategy):
            return False
        if not CoordinatorAgent._rollback_ttl_eligible(strategy):
            return False
        return True

    @staticmethod
    def _max_risk(a: RiskLevel, b: RiskLevel) -> RiskLevel:
        return _RISK_REVERSE[max(_RISK_ORDER[a], _RISK_ORDER[b])]

    @staticmethod
    def _build_decision_reasoning(
        *,
        rationale: str,
        reason: str,
        unresolved_count: int,
        confidence_ok: bool,
        high_risk: bool,
        risk_level: RiskLevel,
        findings: RedTeamFindings | None,
        metadata: PlannerTopologyMetadata | None,
        status: StrategyStatus,
        policy_validation: PolicyValidation,
        safety_checks: list[SafetyCheck] | None = None,
        auto_execution_allowed: bool = True,
        human_approval_required: bool = False,
    ) -> str:
        """生成多行决策推理，便于前端在审计页直接展示。"""
        lines: list[str] = []
        lines.append(f"reason={reason}; risk_level={risk_level.value}")
        lines.append(rationale)
        lines.append(
            f"signals: high_risk={'Y' if high_risk else 'N'}, "
            f"confidence_ok={'Y' if confidence_ok else 'N'}, "
            f"unresolved={unresolved_count}"
        )
        if metadata and metadata.topology_context_used:
            lines.append(
                f"topology: blast_radius={metadata.expected_blast_radius.value}, "
                f"affected_assets={len(metadata.affected_assets)}"
            )
        if findings:
            if findings.residual_attack_paths:
                lines.append(f"residual_attack_paths={len(findings.residual_attack_paths)}")
            if findings.business_impact_risks:
                lines.append(f"business_impact_risks={len(findings.business_impact_risks)}")
        # Phase 3 signals: surface policy MCP validation outcome.
        if policy_validation.mcp_called:
            lines.append(
                f"policy: valid={policy_validation.valid}, "
                f"violations={len(policy_validation.violations)}, "
                f"warnings={len(policy_validation.warnings)}, "
                f"human_approval={policy_validation.requires_human_approval}"
            )
            if policy_validation.mcp_error:
                lines.append(f"policy_error={policy_validation.mcp_error}")
        # Phase 5 signals: human-approval boundary outcome.
        if safety_checks is not None:
            failed_ids = [c.id for c in safety_checks if not c.passed]
            lines.append(
                f"safety: total={len(safety_checks)}, "
                f"failed={len(failed_ids)}"
                + (f" ({', '.join(failed_ids)})" if failed_ids else "")
            )
            lines.append(
                f"approval: human_required={'Y' if human_approval_required else 'N'}, "
                f"auto_execution={'Y' if auto_execution_allowed else 'N'}"
            )
        lines.append(f"status={status.value}")
        return " | ".join(lines)
